package com.truvenhealth.design.standard.domain;

public class ComboForm {

	private String comboOption;
	private String state;
	private String country;
	private String disabledCombo;

	public String getComboOption() {
		return comboOption;
	}

	public void setComboOption(String comboOption) {
		this.comboOption = comboOption;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getDisabledCombo() {
		return disabledCombo;
	}

	public void setDisabledCombo(String disabledCombo) {
		this.disabledCombo = disabledCombo;
	}
	
}
