package com.truvenhealth.design.standard.domain;

public class RadioForm {

	private String radioOption;
	private String disabledRadioOption;
	
	public String getRadioOption() {
		return radioOption;
	}

	public void setRadioOption(String radioOption) {
		this.radioOption = radioOption;
	}

	public String getDisabledRadioOption() {
		return disabledRadioOption;
	}

	public void setDisabledRadioOption(String disabledRadioOption) {
		this.disabledRadioOption = disabledRadioOption;
	}

	
}
